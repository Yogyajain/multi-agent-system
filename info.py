# SQLite database file path (can be set via environment variable, defaults to database.db)
DB_PATH = os.getenv("DB_PATH", "database.db")

# Create SQLite connection string
DATABASE_URL = f"sqlite:///{DB_PATH}"

# Create the SQLAlchemy engine for SQLite
engine = create_engine(
    DATABASE_URL,
    connect_args={"check_same_thread": False},  # Allow SQLite to be used in multi-threaded environments
    echo=False,
)

# Create session factory
SessionLocal = sessionmaker(autocommit=False, autoflush=False, bind=engine)


@contextmanager
def get_db() -> Generator[Any, None, None]:
    """
    Context manager for database sessions.
    Properly handles session creation and cleanup.
    
    Usage:
        with get_db() as db:
            result = db.execute(text("SELECT * FROM users"))
            data = result.fetchall()
    """
    db = SessionLocal()
    try:
        yield db
        db.commit()
    except Exception as e:
        db.rollback()
        raise e
    finally:
        db.close()


def execute_query(query_string: str) -> list:
    """
    Execute a SQL query dynamically and return results.
    This function is designed to work with LLM-generated queries.
    
    Args:
        query_string: SQL query string (can be generated by LLM)
        
    Returns:
        List of result rows (as tuples or Row objects)
        
    Raises:
        Exception: If query execution fails
    """
    try:
        with get_db() as db:
            query = text(query_string)
            result = db.execute(query)
            return result.fetchall()
    except Exception as e:
        raise Exception(f"Query execution failed: {str(e)}")


def run_sql_query(sql_query: str) -> list:
    """
    Execute a SQL query and return the results.
    
    This is a simple wrapper function that takes a SQL query string,
    executes it against the database, and returns the results as a list.
    
    Args:
        sql_query: SQL query string to execute
        
    Returns:
        List of result rows. Each row is returned as a Row object that can be
        accessed by index or column name.
        
    Raises:
        Exception: If query execution fails
        
    Example:
        >>> results = run_sql_query("SELECT * FROM users LIMIT 5")
        >>> for row in results:
        ...     print(row[0])  # Access by index
        ...     print(row['username'])  # Access by column name
    """
    try:
        with get_db() as db:
            query = text(sql_query)
            result = db.execute(query)
            rows = result.mappings().all()
            return rows
    except Exception as e:
        raise Exception(f"Query execution failed: {str(e)}")

